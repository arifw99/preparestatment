    public void koneksi(){
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/latihan_db", "root", "");
            
            statement = (Statement) conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            String sql="SELECT * FROM biodata_mhs";
            result=statement.executeQuery(sql);
            
            result.first();
            tampilData();
            
        } catch (IllegalAccessException | ClassNotFoundException | InstantiationException | SQLException ex ) {
            Logger.getLogger(FormBiodata.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void tampilData() throws SQLException{
        txtNim.setText(result.getString("nim"));
        txtNama.setText(result.getString("nama"));
        String jk_ = result.getString("jk");
        switch (jk_){
            case"Laki-laki":
                rbLaki.setSelected(true);
                break;
            case"Perempuan":
                rbPr.setSelected(true);
                break;
        }
        txtTglL.setText(result.getString("tanggal_lahir"));
        txtAlamat.setText(result.getString("alamat"));
        txtFakultas.setText(result.getString("fakultas"));
        txtJurusan.setText(result.getString("jurusan"));
        txtAngkatan.setText(result.getString("angkatan"));
       btnSaveR.setEnabled(true);
    }
    
