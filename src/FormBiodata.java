    private void btnSaveRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveRActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/latihan_db", "root", "");

            statement = (Statement) conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_UPDATABLE);
            String sql="SELECT * FROM biodata_mhs";
            result=statement.executeQuery(sql);

            while (result.next()){
                result.moveToInsertRow();
                result.updateString("nim", txtNim.getText());
                result.updateString("nama", txtNama.getText());
                String jk_ = null;
                if(rbLaki.isSelected())
                jk_ = "Laki-laki";
                else if(rbPr.isSelected())
                jk_ = "Perempuan";
                result.updateString("jk", jk_);

                SimpleDateFormat frmtDate = new SimpleDateFormat("dd/MM/yyyy");
                try{
                    java.util.Date date = frmtDate.parse(txtTglL.getText());
                    java.sql.Date sqlDate = new java.sql.Date(date.getTime());
                    result.updateDate("tanggal_lahir",(java.sql.Date)sqlDate);
                }catch (ParseException ex){
                }
                result.updateString("alamat", txtAlamat.getText());
                result.updateString("fakultas", txtFakultas.getText());
                result.updateString("jurusan", txtJurusan.getText());
                result.updateString("angkatan", txtAngkatan.getText());

                result.insertRow();

                JOptionPane.showMessageDialog(null, "Data berhasil ditambah");
            }

        } catch (IllegalAccessException | ClassNotFoundException | InstantiationException | SQLException ex ) {
            Logger.getLogger(FormBiodata.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            koneksi();
        }

        btnSimpan.setEnabled(true);
        btnDelete.setEnabled(true);
        btnAdd.setEnabled(true);
        btnSaveR.setEnabled(false);
    }//GEN-LAST:event_btnSaveRActionPerformed

